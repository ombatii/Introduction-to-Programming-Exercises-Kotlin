package kotlinPractice.programming_basics

fun main() {
    /**
     * This program displays your name and complete mailing address formatted in
     * the manner that you would usually see it on the outside of an envelope.
     * The program does not need to read any input from the user.
     */
    val name = "Brian Ombati"
    val streetAddress = "1234 Winner Street"
    val city = "Nairobi"
    val state = "Kenya"
    val postalCode = "56100"

    println("Name: $name")
    println("Street Address: $streetAddress")
    println("City: $city")
    println("State: $state")
    println("Postal Code: $postalCode")
}
